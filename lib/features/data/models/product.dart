class Product {
  final int id;
   int count;
  final String name;
  final String description;
  final String image;
  double price;
  final String unit;
  final double rating;

  Product({
    required this.id,
    required this.count ,
    required this.name,
    required this.description,
    required this.image,
    required this.price,
    required this.unit,
    required this.rating,
  });
}
